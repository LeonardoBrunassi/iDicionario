}

- (NSInteger)tableView:(UITableView *)tableView numberOfRowsInSection:(NSInteger)section {
#warning Incomplete method implementation.
    // Return the number of rows in the section.
    return [dic getCount];
}


- (UITableViewCell *)tableView:(UITableView *)tableView cellForRowAtIndexPath:(NSIndexPath *)indexPath {
    UITableViewCell *cell = [[UITableViewCell alloc] initWithStyle:UITableViewCellStyleSubtitle reuseIdentifier:@"default"];
    
    long row = indexPath.row;
    // Configure the cell...
    
    [cell.textLabel setText:[dic retornoLetra:row]];
    [cell.detailTextLabel setText:[dic retornoPalavra:row]];
    [cell.imageView setImage:[dic retornoImagem:row]];
    return cell;
}


